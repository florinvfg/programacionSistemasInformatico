package Unidad3;
import java.util.*;
/*Escribir todos los multiplos de 7 menores que 100*/
public class ActividadResuelta3_7 {
    public static void main(String[] args) {
        /*Vamos a utilizar un bucle for, iniciando la i a 0, e iterando hasta que el valor
        * supere 100.Los multiplos de 7, se caraterizan por que se diferencian en 7.*/

        for (int i=0; i<100; i+=7){
            System.out.println(i);
        }
/*Cuando el bloque de instruciones de for, while o do-while esta formando
* por una sola instruccion,no precisa de llaves{}.Aunque, por la claridad en el codico, se aconseja ponerlas*/


    }
}
